export const getShade = (color, colorcode) => {
	switch (colorcode) {
	case `50`:
	case `100`:
	case `200`:
	case `300`:
	case `400`:
	case `500`:
	case `600`:
	case `700`:
	case `800`:
	case `900`:
	case `100A`:
	case `200A`:
	case `400A`:
	case `700A`:
		break
	default:
		console.warn(`Color code must be a valid material design color code. Defaulting to 500..`)
		colorcode = `500`
		break
	}
	return Object.keys(colors).reduce((acc, key) => Object.values(colors[key]).includes(color) ? colors[key][colorcode] : acc, color)
}

export const getColor = (usedColors = [], colorcode = `500`) => {
	switch (colorcode) {
	case `50`:
	case `100`:
	case `200`:
	case `300`:
	case `400`:
	case `500`:
	case `600`:
	case `700`:
	case `800`:
	case `900`:
	case `100A`:
	case `200A`:
	case `400A`:
	case `700A`:
		break
	default:
		console.warn(`Color code must be a valid material design color code. Defaulting to 500..`)
		colorcode = `500`
		break
	}
	const keys = Object.keys(colors)
	let color = ``
	do color = colors[keys[Math.floor(Math.random() * keys.length)]][colorcode]
	while(usedColors.includes(color))
	return color
}

